x1 = [10.0, 10.0, 10.0, -100.0, 1000.0]
y1 = [20.0, 100.0, -10.0, 10.0, 1000.0]
x2 = [0.0] * 5
y2 = [0.0] * 5
m2 = [500.0, 400.0, 300.0, 200.0, 100000000000.0]

def calc_grav(x1, y1, x2, y2, m2):
    r = ((x1 - x2) ** 2 + (y1 - y2) ** 2) ** 0.5
    F = m2 / r ** 3
    return F * (x1 - x2), F * (y1 - y2)

for i in range(5):
    print(calc_grav(x1[i], y1[i], x2[i], y2[i], m2[i]))